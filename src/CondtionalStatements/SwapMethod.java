package CondtionalStatements;

public class SwapMethod {

	public static void main(String[] args) {
		int a=10;
		int b=2;
		int c=3;
		int d=4;
		int temp;
		temp=a;
		a=b;
		b=c;
		c=d;
		d=temp;
		
		System.out.println("after swap  the value of a:"+a);
		System.out.println("after swap  the value of b:"+b);
		System.out.println("after swap  the value of c:"+c);
		System.out.println("after swap  the value of d:"+d);

	}

}
